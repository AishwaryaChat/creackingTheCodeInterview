/*
Max Product Subarray

Problem Description
Given an integer array A of size N. Find the contiguous subarray within the given array (containing at least one number) which has the largest product.

Return an integer corresponding to the maximum product possible.

NOTE: Answer will fit in 32-bit integer value.



Problem Constraints
1 <= N <= 5 * 10^5

-100 <= A[i] <= 100



Input Format
First and only argument is an integer array A.



Output Format
Return an integer corresponding to the maximum product possible.



Example Input
Input 1:

 A = [4, 2, -5, 1]
Input 2:

 A = [-3, 0, -5, 0]


Example Output
Output 1:

 8
Output 2:

 0


Example Explanation
Explanation 1:

 We can choose the subarray [4, 2] such that the maximum product is 8.
Explanation 2:

 0 will be the maximum product possible.
*/

// The idea here is to keep track of both max and min product at each step
// This is because if two numbers are multiplied out of which one is negative and another is possitime, but the absolute value is greater then there is a possibility that next another negative number comes and makes it a bigger values
// TC = O(N)
// SC = O(1)

function solve(A) {
  let currMax = 1
  let currMin = 1
  let max = Math.max(...A)
  for (let i = 0; i < A.length; i++) {
    if(A[i] === 0) {
        currMax = 1
        currMin = 1
        continue
    }
    let temp = A[i] * currMax
    currMax = Math.max(A[i] * currMax, A[i] * currMin, A[i])
    currMin = Math.min(temp, A[i] * currMin, A[i])
    max = Math.max(max, currMax)
  }
  return max
}

