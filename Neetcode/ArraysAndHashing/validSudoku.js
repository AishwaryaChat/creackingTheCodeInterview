// ,[".",".",".","4","1","9",".",".","5"]
// ,[".",".",".",".","8",".",".","7","9"]]
// Output: false
// Explanation: Same as Example 1, except with the 5 in the top left corner being modified to 8. Since there are two 8's in the top left 3x3 sub-box, it is invalid.

// Constraints:

// board.length == 9
// board[i].length == 9
// board[i][j] is a digit 1-9 or '.'.

// TC - O(N^2)
// SC - O(N^2)
function isValidSquare(map, i, j, box, character) {
  if (map.rows[i][character]) return false;
  if (map.cols[j][character]) return false;
  if (map.boxes[box][character]) return false;
  return true;
}

function solve(board) {
  let map = { rows: {}, cols: {}, boxes: {} };
  for (let i = 0; i < 9; i++) {
    for (let j = 0; j < 9; j++) {
      const character = board[i][j];
      if (character !== ".") {
        const boxId = Math.floor(i / 3) * 3 + Math.floor(j / 3);
        if (!map.rows[i]) map.rows[i] = {};
        if (!map.cols[j]) map.cols[j] = {};
        if (!map.boxes[boxId]) map.boxes[box] = {};
        if (!isValidSquare(map, i, j, boxId, character)) return false;
        map.rows[i][character] = true;
        map.cols[j][character] = true;
        map.boxes[boxId][character] = true;
      }
    }
  }
  return true;
}

